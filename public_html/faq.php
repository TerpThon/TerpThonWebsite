<h2 name="HowDoIRegister">How do I register for Terp Thon?</h2>
<p><a href="http://events.dancemarathon.com/index.cfm?fuseaction=donorDrive.event&eventID=1160" target="_blank">Click here</a> or click "Register Here" on the home page to be rerouted to our Donor Drive fundraising portal. Once there click "Register Now" and choose your team, create a new one or dance as an individual. Registration is free! We request that all dancers raise a minimum of $120.</p>

<h2 name="DanceWholeTime">Do I have to dance the whole time?</h2>
<p>No, you are not required to dance for the 12 hours. Actually, you're not required to dance at all. There is definitely a lot of dancing going on at the Dance Marathon event, but you simply pledge to stand on your feet for 12 hours straight. There are other activities besides dancing to fill your time. There are <a href="/miraclekids.php">Miracle Kids</a> to interact with along with fun games, arts and crafts and plenty of time to hang out with friends and meet new people.</p>

<h2 name="DancerReqs">What are the requirements to be a dancer?</h2>
<p>We ask that each participant is a UMD student, raises $120 or more and stays for the entire 12 hours. In return, you receive two meals, a t-shirt, 12 hours of fun and entertainment, and memories to last a lifetime. </p>

<h2 name="BringingDonations">Can I bring my $120 to the event?</h2>
<p>We encourage participants to raise their money online because it easier for us to keep track of. However, we will allow those already signed up on our website to bring their money to the Dance Marathon event in an envelope with your name on it. </p>

<h2 name="WhatToBring">What should I bring?</h2>
<p>You won't need to bring much and please don't bring anything valuable. You should bring a water bottle and maybe a change of socks. Wear comfortable clothes like gym shorts and sneakers. A free T-shirt will be provided with your team's color. Feel free to over-accessorize with your team's color. Bandanas, hats, sunglasses, costume jewelry and face paint are highly encouraged and spirit points will be awarded to those who go above and beyond!</p>

<h2 name="WhatDoWeDo">What do we do for 12 hours?</h2>
<p>There will be a full schedule for the 12-hour Dance Marathon. The time will be filled with music, performances, Miracle Kid stories, games and two meal times.</p>

<h2 name="NotInOrg">What if I am not part of an organization that participates in Terp Thon?</h2>
<p>Anyone can sign up to be a dancer for Terp Thon and involvement within another organization is not required. Teams can be made when you register at <a href="http://events.dancemarathon.com/index.cfm?fuseaction=donorDrive.event&eventID=1160"
>events.dancemarathon.com/event/terpthon</a>. They can consist of anywhere from 1 to 1,000,000 people, so you can grab a group of friends, your roommates or dance as an individual. </p>

<h2 name="JudgeDancing">Do you judge on dancing skills?</h2>
<p>No....and yes. There is no skill level required for dancing at the event. However, winning a dance off might earn your team some extra spirit points!</p>

<h2 name="Eligible">Who is eligible to register?</h2>
<p>Any undergraduate student at the University of Maryland is able to sign up as a dancer for the spring Dance Marathon event. </p>

<h2 name="RaisingMoney">How do I raise my $120?</h2>
<p>There are endless ways to raise your minimum!
<ul id="RaisingMoneyList" style="text-align: justify">
<li>You can ask friends and families to donate to your personal fundraising page on <a href="http://events.dancemarathon.com/index.cfm?fuseaction=donorDrive.event&eventID=1160">events.dancemarathon.com/event/terpthon</a>.</li>

<li>You can go canning! Canning is when you go out in public and talk to people about the cause and ask for small donations. You can go canning in your hometown, Washington D.C., on Route 1, or basically anywhere! Email Elizabeth McKinley at <a href="mailto:fundraising@terpthon.org">fundraising@terpthon.org</a> for more information. Many dancers raise their $120 minimum and more during a few hours of canning.</li>

<li>You can host your own fundraising event, like a bake sale. Many student organizations and Terp Thon teams host their own events like a sports tournament, car washes or restaurant nights to benefit their fundraising total. </li>

<li>Put up a flyer in your workplace or your parent's workplace to reach co-workers as well.</li>

<li>The ideas for fundraising are endless. Let us know how we can help by sending an email to Elizabeth McKinley at <a href="mailto:fundraising@terpthon.org">fundraising@terpthon.org</a>.</li></p>
</ul>
<h2 name="WhyStand">Why do we stand for 12 hours?</h2>
<p>We pledge to stand for 12 hours to represent the long nurses' shifts at the hospital.</p>

<h2 name="CannotStand">What if I cannot stand for 12 hours, can I still be involved?</h2>
<p>You can 100% still be involved. If you are interested in being a dancer, but for health reasons cannot stand for the full 12 hours, we can make accommodations. Contact our Executive Director, Bianca Fiore at <a href="mailto:executivedirector@terpthon.org">executivedirector@terpthon.org</a> with any concerns. Another way to get involved is to be a volunteer. For the day of event, we will be recruiting volunteers to help with different tasks. These shifts will only be a few hours long and are crucial to making the event run smoothly. If you are interested, contact Brenna Hohl at <a href="mailto:operations@terpthon.org">operations@terpthon.org</a></p>

<h2 name="BusinessInvolved">How can I involve my business with Terp Thon?</h2>
<p>There are plenty of ways to become a Terp Thon sponsor, no matter how large or small the involvement! You can visit our <a href="/sponsors.php">Sponsors Tab</a> to contact either our Hospitality or Corporate Relations Chair to inquire about sponsorship opportunities.</p>

<h2 name="BusinessBenefit">What can my business get out of Terp Thon?</h2>
<p>A lot of exposure to one of the largest campuses in the U.S. depending on the level of involvement. This can be expanded upon with a simple email to our Hospitality or Corporate Relations chairs.</p>



