<h1>Students</h1>
The term "canning" is the word that us Dance Marathoners use when we refer to canister solicitation. This is an incredibly valuable tool in your fundraising efforts when done properly. We will provide you with the canning materials, and an internal Terp Thon member will accompany you to assist as well. A total of 10 people may go canning per date listed (not including the two internal members), with 5 of those at most being from one group/organization. <a target= "_blank" href="https://docs.google.com/spreadsheets/d/1NCHa3_BWbdzGUfyb2WAIrld6L96Ty1afx0jocqWgaIc/edit#gid=0"> This link </a>will lead you to a Google Spreadsheet with the dates of athletic events we will be canning, and the people signed up for that day, 'MS' stands for Men's Soccer while 'F' stands for Football Please complete <a target= "_blank" href="https://docs.google.com/forms/d/1wym1wXEWEzRXNvv1jXm6rb58HTaQcPnAIub2g5S5wJ0/viewform?usp=send_form"> this </a> Google Form to sign up for canning. If you are with a group, please indicate so and choose one person as the contact to which we can give more information. 
<br />
<h4>Some canning tips and guidelines for ya!</h4>

<ol>
<li><b>Know your stuff.</b> know what Terp Thon is and what we do. The internal members canning with you will give you a small spiel before you begin, and we will provide small strips with a Terp Thon elevator pitch as well.</li>
<li><b>Wear Terp Thon gear.</b> If you have it, flaunt it! If not, ask a friend if you may borrow a shirt. Then again, you could always buy one through us, FTK as usual!</li>
<li><b>Have fun!</b> People want to donate to fun, outgoing, and passionate people. Going along with the last tip, we actually find that people who wear costumes tend to pull in the most money #ForTheKids. </li>
<li><b>Be polite.</b> Don't overwhelm people or bug them. If they don't want to donate, they'll either make it perfectly clear or you'll just get the feeling. Either way, know which direction to take the conversation. Reply with: "Thanks for listening, check out www.TerpThon.org and like us on Facebook. Have a good day!"</li>
<li>Please remember you are representing Terp Thon while canning. We are a professional organization raising money for children, so <b>keep it appropriate!</b></li>
</ol>
<br />

